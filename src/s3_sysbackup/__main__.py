import shutil
import sys
from .program import main

if __name__ == '__main__':
    interp = 'python3'
    if shutil.which(interp) != sys.executable:
        interp = sys.executable
    sys.argv[0] = "{} -m {}".format(interp, __package__)
    main(sys.argv[1:])
